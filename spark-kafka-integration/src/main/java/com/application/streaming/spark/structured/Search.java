package com.application.streaming.spark.structured;

public class Search {
	private String ad_group;
	private String device;
	private String search_engine;
	private String cost;
	private String clicks;
	
	public String getAd_group() {
		return ad_group;
	}
	public void setAd_group(String ad_group) {
		this.ad_group = ad_group;
	}
	public String getDevice() {
		return device;
	}
	public void setDevice(String device) {
		this.device = device;
	}
	public String getSearch_engine() {
		return search_engine;
	}
	public void setSearch_engine(String search_engine) {
		this.search_engine = search_engine;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public String getClicks() {
		return clicks;
	}
	public void setClicks(String clicks) {
		this.clicks = clicks;
	}
}
